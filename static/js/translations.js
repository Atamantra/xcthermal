export function setLanguage(lang) {
    if (!translations[lang]) return;

    // Save preference
    localStorage.setItem('userLanguage', lang);

    // Update elements with data-i18n
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' && el.getAttribute('placeholder')) {
                el.placeholder = translations[lang][key];
            } else {
                // Check if it has child nodes that shouldn't be overwritten (like icons)
                // For simplicity, we assume text-only or specific structure handled manually if complex.
                // Actually, let's use a safer approach for mixed content if needed, but strict text replacement is easiest.
                // Some buttons have icons. e.g. <img src...> Text. 
                // We should inspect the content. If it has an image, we might want to target a span inside.
                // Best practice: Wrap text in <span data-i18n="key">Text</span>.
                el.textContent = translations[lang][key];
            }
        }
    });

    // Update <html lang="...">
    document.documentElement.lang = lang;

    // Dispatch event for other components to react
    window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}
